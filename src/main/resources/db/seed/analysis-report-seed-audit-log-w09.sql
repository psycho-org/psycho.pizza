resolved_audit as (
    select
        s.column1::uuid as id,
        '10000000-0000-0000-0000-000000000009'::uuid as workspace_id,
        s.column2::uuid as actor_id,
        s.column3 as target_type,
        case
            when s.column3 = 'TASK' then t.id
            when s.column3 = 'SPRINT' then sp.id
            else null
        end as target_id,
        s.column5 as event_type,
        s.column6 as from_value,
        s.column7 as to_value,
        s.column8::timestamp as occurred_at
    from audit_seed s
    left join task_lookup t
        on s.column3 = 'TASK'
       and t.title = s.column4
    left join sprint_lookup sp
        on s.column3 = 'SPRINT'
       and sp.name = s.column4
)
insert into public.audit_log (
    id,
    workspace_id,
    actor_id,
    target_type,
    target_id,
    event_type,
    from_value,
    to_value,
    occurred_at,
    created_at,
    updated_at
)
select
    ra.id,
    ra.workspace_id,
    ra.actor_id,
    ra.target_type,
    ra.target_id,
    ra.event_type,
    ra.from_value,
    ra.to_value,
    ra.occurred_at,
    now(),
    now()
from resolved_audit ra
where ra.target_id is not null
  and not exists (
      select 1
      from public.audit_log al
      where al.id = ra.id
  );
