normalized_seed as (
    select
        workspace_id::uuid as workspace_id,
        sprint_id::uuid as sprint_id,
        ordinality as project_order,
        (
            substr(md5(sprint_id || ':project:' || ordinality), 1, 8) || '-' ||
            substr(md5(sprint_id || ':project:' || ordinality), 9, 4) || '-' ||
            substr(md5(sprint_id || ':project:' || ordinality), 13, 4) || '-' ||
            substr(md5(sprint_id || ':project:' || ordinality), 17, 4) || '-' ||
            substr(md5(sprint_id || ':project:' || ordinality), 21, 12)
        )::uuid as project_id,
        (
            substr(md5(sprint_id || ':mapping:' || ordinality), 1, 8) || '-' ||
            substr(md5(sprint_id || ':mapping:' || ordinality), 9, 4) || '-' ||
            substr(md5(sprint_id || ':mapping:' || ordinality), 13, 4) || '-' ||
            substr(md5(sprint_id || ':mapping:' || ordinality), 17, 4) || '-' ||
            substr(md5(sprint_id || ':mapping:' || ordinality), 21, 12)
        )::uuid as mapping_id
    from project_seed
    cross join lateral unnest(array[project_1, project_2, project_3, project_4, project_5]) with ordinality as p(project_name, ordinality)
    where project_name is not null
)
insert into public.sprint_project_mapping (
    id,
    created_at,
    updated_at,
    sprint_id,
    project_id,
    workspace_id
)
select
    n.mapping_id,
    now(),
    now(),
    n.sprint_id,
    n.project_id,
    n.workspace_id
from normalized_seed n
where not exists (
    select 1
    from public.sprint_project_mapping spm
    where spm.id = n.mapping_id
);
