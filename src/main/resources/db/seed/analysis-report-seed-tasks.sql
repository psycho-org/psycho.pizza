    from public.tasks t
    where t.id = v.id::uuid
);

with project_base as (
    select
        p.id as project_id,
        p.name as project_name,
        split_part(p.name, ' - ', 2) as project_story_name,
        p.workspace_id,
        spm.sprint_id,
        s.start_date,
        s.end_date,
        coalesce(nullif(substring(p.name from 'P([0-9]{2})'), ''), '01')::int as project_order,
        mod(abs(hashtext(p.id::text)), 5) + 4 as task_count
    from public.projects p
    join public.sprint_project_mapping spm
        on spm.project_id = p.id
       and spm.workspace_id = p.workspace_id
    join public.sprints s
        on s.id = spm.sprint_id
       and s.workspace_id = p.workspace_id
    where p.name like 'W%'
),
workspace_members as (
    select
        m.workspace_id,
        m.account_id,
        row_number() over (
            partition by m.workspace_id
            order by case when m.role = 'OWNER' then 0 else 1 end, m.account_id
        ) as member_seq,
        count(*) over (partition by m.workspace_id) as member_count
    from public.memberships m
    where m.deleted_at is null
      and m.deleted_by is null
),
audit_project_overrides as (
    select *
    from (
        values
            ('10000000-0000-0000-0000-000000000007'::uuid, 'W07-S03 P03 - 정산 반영 항목 보정 - 실행 준비 - 정산 반영 항목 보정 실행 전 준비 사항 점검', 'W07-S03 P04 - 운영 일정 재수립'),
            ('10000000-0000-0000-0000-000000000008'::uuid, 'W08-S03 P03 - 후속 점검 항목 추가 - 실행 준비 - 후속 점검 항목 추가 실행 전 준비 사항 점검', 'W08-S04 P02 - 운영 재개 체크리스트 정리'),
            ('10000000-0000-0000-0000-000000000009'::uuid, 'W09-S03 P02 - 우선순위 충돌 조정 - 실행 준비 - 우선순위 충돌 조정 실행 전 준비 사항 점검', 'W09-S04 P03 - 회의체 재구성'),
            ('10000000-0000-0000-0000-000000000010'::uuid, 'W10-S03 P03 - 종료 전 이관 목록 정리 - 현황 및 자료 수집 - 종료 전 이관 목록 정리 관련 자료와 선행 이슈 정리', 'W10-S03 P01 - 서비스 축소 범위 공지')
    ) as v(workspace_id, title, project_name)
),
project_lookup as (
    select
        p.workspace_id,
        p.id as project_id,
        p.name as project_name
    from public.projects p
    where p.name like 'W%'
),
task_seed_base as (
    select
        pb.workspace_id,
        pb.project_id,
        format(
            '%s - %s - %s',
            pb.project_name,
            case gs.seq
                when 1 then '현황 및 자료 수집'
                when 2 then '초안 정리'
                when 3 then '검토 및 합의'
                when 4 then '실행 준비'
                when 5 then '운영 반영'
                when 6 then '결과 점검'
                when 7 then '후속 보완'
                else '마감 정리'
            end,
            case gs.seq
                when 1 then format('%s 관련 자료와 선행 이슈 정리', pb.project_story_name)
                when 2 then format('%s 초안과 진행 방향 구체화', pb.project_story_name)
                when 3 then format('%s 검토 의견 취합 및 합의', pb.project_story_name)
                when 4 then format('%s 실행 전 준비 사항 점검', pb.project_story_name)
                when 5 then format('%s 운영 반영 및 현장 적용', pb.project_story_name)
                when 6 then format('%s 결과 확인과 이슈 점검', pb.project_story_name)
                when 7 then format('%s 후속 보완안 정리', pb.project_story_name)
                else format('%s 마감 정리 및 공유', pb.project_story_name)
            end
        ) as title,
        gs.seq as task_order,
        (
            substr(md5(pb.project_id::text || ':task:' || gs.seq), 1, 8) || '-' ||
            substr(md5(pb.project_id::text || ':task:' || gs.seq), 9, 4) || '-' ||
            substr(md5(pb.project_id::text || ':task:' || gs.seq), 13, 4) || '-' ||
            substr(md5(pb.project_id::text || ':task:' || gs.seq), 17, 4) || '-' ||
            substr(md5(pb.project_id::text || ':task:' || gs.seq), 21, 12)
        )::uuid as task_id,
        (
            substr(md5(pb.project_id::text || ':task-mapping:' || gs.seq), 1, 8) || '-' ||
            substr(md5(pb.project_id::text || ':task-mapping:' || gs.seq), 9, 4) || '-' ||
            substr(md5(pb.project_id::text || ':task-mapping:' || gs.seq), 13, 4) || '-' ||
            substr(md5(pb.project_id::text || ':task-mapping:' || gs.seq), 17, 4) || '-' ||
            substr(md5(pb.project_id::text || ':task-mapping:' || gs.seq), 21, 12)
        )::uuid as mapping_id
    from project_base pb
    cross join lateral generate_series(1, pb.task_count) as gs(seq)
    join workspace_members wm
        on wm.workspace_id = pb.workspace_id
       and wm.member_seq = ((pb.project_order + gs.seq - 2) % wm.member_count) + 1
),
task_seed as (
    select
        tsb.workspace_id,
        coalesce(pl.project_id, tsb.project_id) as project_id,
        tsb.task_id,
        tsb.mapping_id
    from task_seed_base tsb
    left join audit_project_overrides apo
        on apo.workspace_id = tsb.workspace_id
       and apo.title = tsb.title
    left join project_lookup pl
        on pl.workspace_id = tsb.workspace_id
       and pl.project_name = apo.project_name
)
insert into public.project_task_mapping (
    id,
    created_at,
    updated_at,
    project_id,
    task_id,
    workspace_id
)
select
    ts.mapping_id,
    now(),
    now(),
    ts.project_id,
    ts.task_id,
    ts.workspace_id
from task_seed ts
where not exists (
    select 1
    from public.project_task_mapping ptm
    where ptm.id = ts.mapping_id
);
