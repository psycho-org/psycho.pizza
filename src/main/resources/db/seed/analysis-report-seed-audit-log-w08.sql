-- 로그 집계 요약:
-- - 직접 감점 후보 로그 수: 8건
--   - Due date 이후 DONE 처리 2건
--   - DONE -> IN_PROGRESS 리워크 2건
--   - IN_PROGRESS -> TODO 상태 회귀 1건
--   - CANCELED 처리 1건
--   - TODO -> DONE 직행 1건
--   - TASK_PROJECT_CHANGED 1건
-- - 판단 유보 조정 로그 수: 11건
--   - SPRINT_GOAL_CHANGED 1건
--   - TASK_DUE_DATE_CHANGED 2건
--   - TASK_ASSIGNEE_CHANGED 3건
--   - TASK_ADDED_TO_SPRINT 5건
-- - 단순 진행 로그 수: 13건
--   - TASK_STATUS_CHANGED 13건
--
-- 의도:
-- 1) W08은 평시 운영 점검으로 시작했지만, 장애 반복 이후 복구/재개/후속 공지가 우선되는 시나리오다.
-- 2) 따라서 incident 이후 생성되었어야 할 대응 범위는 Sprint 02 목표 전환과 이후 Task 추가/이동으로 근사 표현한다.
--    현재 enum에는 PROJECT_CREATED / SPRINT_CREATED 이벤트가 없으므로 audit_log에는 생성 자체를 직접 기록하지 않는다.
-- 3) 특히 장애 직후에는 기존 계획에 없던 재발 방지/온콜 조정 태스크가 Sprint 02에 뒤늦게 배치되는 패턴을 넣는다.
-- 4) 한 번 끝난 작업도 장애 재발로 다시 열리고, 고객 공지와 운영 재개 작업이 중간에 추가되며,
--    root cause 파악 작업은 마감이 밀린다.
-- 5) 평시 점검 -> 긴급 장애 접수 -> 원인 추적 및 재발 방지 -> 고객 공지 및 운영 재개 흐름이
--    감사 로그에서도 읽히도록 이벤트 시점을 순차 배치한다.
-- 6) 이 파일의 "직접 감점 후보"와 "판단 유보 조정 로그" 분류는 seed 작성 편의용 메모다.
--    최종 감점 여부와 추가 연쇄 감점 적용 여부는 서버의 scoring 로직이 판단한다.
--
-- 담당자 적합성 판단 기준:
-- - 이 파일에서의 담당자 해석은 DB 스키마의 공식 직무 컬럼에 근거한 것이 아니다.
-- - 현재 스키마만으로는 직군을 확정할 수 없으므로,
--   src/main/resources/db/seed/analysis-report-seed-data-planning.md 의 account key(A01~A30) 분포표와 seed email 규칙을 seed 해석 기준으로 사용한다.
