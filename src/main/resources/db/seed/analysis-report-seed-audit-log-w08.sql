    ra.id,
    ra.workspace_id,
    ra.actor_id,
    ra.target_type,
    ra.target_id,
    ra.event_type,
    ra.from_value,
    ra.to_value,
    ra.occurred_at,
    now(),
    now()
from resolved_audit ra
where ra.target_id is not null
  and not exists (
      select 1
      from public.audit_log al
      where al.id = ra.id
  );
