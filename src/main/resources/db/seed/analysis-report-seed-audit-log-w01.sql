-- 3) 대부분 TODO -> IN_PROGRESS -> DONE 순으로 이동하며,
--    담당자 변경도 단 1회만, 그것도 더 적합한 담당자에게 넘기는 건강한 패턴으로 구성한다.
-- 4) 스프린트별 업무 스토리(온보딩 기획 -> 운영안 정비 -> 자동 안내 체계 -> 품질/정산 안정화)가
--    감사 로그에서도 자연스럽게 읽히도록 이벤트 시점을 순차 배치한다.
-- 5) 이 파일의 "직접 감점 후보"와 "판단 유보 조정 로그" 분류는 seed 작성 편의용 메모다.
--    최종 감점 여부와 추가 연쇄 감점 적용 여부는 서버의 scoring 로직이 판단한다.
--
-- 담당자 적합성 판단 기준:
-- - 이 파일에서 말하는 "더 적합한 담당자"는 DB 스키마의 공식 컬럼(job_family, department 등)로 판별한 값이 아니다.
-- - 현재 accounts / memberships 스키마만으로는 직군을 확정할 수 없기 때문에,
--   src/main/resources/db/seed/analysis-report-seed-data-planning.md 의 account key(A01~A30) 분포표와
--   seed email 규칙을
--   시드 데이터 해석 기준으로 사용한다.
-- - 따라서 아래 ASSIGNEE 변경은 "정산 정보 수집 절차 반영" 업무를
--   오너(A01)에서 사업운영 계열로 가정한 A28(seed+a28@psycho.local)에게 넘기는 시나리오를 의미한다.
-- - 즉, 이것은 실제 운영 DB 사실이 아니라 seed world 안에서의 역할 가정이다.

