resolved_audit as (
    select
        s.column1::uuid as id,
        '10000000-0000-0000-0000-000000000011'::uuid as workspace_id,
        s.column2::uuid as actor_id,
        s.column3 as target_type,
        t.id as target_id,
        s.column5 as event_type,
        s.column6 as from_value,
        s.column7 as to_value,
        s.column8::timestamp as occurred_at
    from audit_seed s
    join task_lookup t
        on t.title = s.column4
)
insert into public.audit_log (
    id,
    workspace_id,
    actor_id,
    target_type,
    target_id,
    event_type,
    from_value,
    to_value,
    event_id,
    occurred_at,
    created_at,
    updated_at
)
select
    ra.id,
    ra.workspace_id,
    ra.actor_id,
    ra.target_type,
    ra.target_id,
    ra.event_type,
    ra.from_value,
    ra.to_value,
    (
        substr(md5(ra.id::text || ':event'), 1, 8) || '-' ||
        substr(md5(ra.id::text || ':event'), 9, 4) || '-' ||
        substr(md5(ra.id::text || ':event'), 13, 4) || '-' ||
        substr(md5(ra.id::text || ':event'), 17, 4) || '-' ||
        substr(md5(ra.id::text || ':event'), 21, 12)
    )::uuid,
    ra.occurred_at,
    now(),
    now()
from resolved_audit ra
where not exists (
    select 1
    from public.audit_log al
    where al.id = ra.id
);
